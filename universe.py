def tickers() -> list[str]:
    return [t for t, _n, _s in UNIVERSE]


def names() -> dict[str, str]:
    return {t: n for t, n, _s in UNIVERSE}


def sectors() -> dict[str, str]:
    """Ticker -> sector-label mapping."""
    return {t: s for t, _n, s in UNIVERSE}


def all_names() -> dict[str, str]:
    """Full lookup table including indices."""
    out = {t: n for t, n, _s in UNIVERSE}
    out.update({t: n for t, n in INDICES})
    return out


