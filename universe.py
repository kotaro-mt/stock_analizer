# NOTE: the raw list above is intentionally a bit over 225 so real
# duplicates (stocks that plausibly fit two sectors — e.g. Daikin in 機械
# and again under サービス) can be pruned. We dedupe by ticker below,
# keeping the FIRST occurrence (the more natural sector assignment).
_seen: set[str] = set()
_deduped: list[tuple[str, str, str]] = []
for _t, _n, _s in UNIVERSE:
    if _t in _seen:
        continue
    _seen.add(_t)
    _deduped.append((_t, _n, _s))
UNIVERSE = _deduped
del _seen, _deduped


